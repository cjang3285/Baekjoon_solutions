#include<bits/stdc++.h>
using namespace std;
int main(){
    long long n;
    cin >> n;
    // 창문 k번은 k의 약수 개수만큼 토글됨
    // 약수는 보통 쌍으로 존재 (ex. 12 = (1,12),(2,6),(3,4)) → 짝수개 → 닫힘
    // 단, 자기 자신과 쌍이 되는 경우 (ex. 9 = (1,9),(3,3)) → 3이 혼자 카운트 → 홀수개
    // 자기 자신과 쌍이 되려면 k×k = n 꼴이어야 함 → k = √n 이 정수여야 함 → 완전제곱수만 약수 홀수개
    // 홀수번 토글 = 열림 → 완전제곱수만 열림
    // N이하 완전제곱수 개수: 1²,2²,...,k² ≤ N → k ≤ √N → k는 1부터 √N까지 → 개수 = √N
    cout << (long long)sqrt((double)n);
}