#include <bits/stdc++.h>
using namespace std;

// 수식은 추상화해서 다음과 같이 볼 수 있다.
// ....+..-...+..-..-.
// 여기서 -만 구별하고 나머지는 한 덩어리로 보자.
// <....+..>-<...+..>-<..>-<.>
// 첫 덩어리는 그대로 ans에 합하면 되고
// 다음 덩어리들은 전부 앞에 -가 붙으니 덩어리를 통째로 빼주면 된다.

// 수식을 추상화하면 다음과 같이 볼 수 있다.
//  ....+..-...+..-..-.
// '-'를 기준으로 나누고, 각 덩어리 안에는 숫자와 '+'만 존재
// <....+..>-<...+..>-<..>-<.>
// 첫 덩어리: 괄호 밖 → 그냥 더하기
// 나머지 덩어리: 괄호 안처럼 처리 → 모든 숫자를 통째로 빼기

int main() {
    string s;
    cin >> s;  
    // 전체 수식을 문자열로 입력받는다.

    vector<string> parts;
    string tmp = "";  
    // '-' 기준으로 나눌 조각을 임시로 저장할 변수

    // 수식을 한 글자씩 순회하며 '-'를 기준으로 분리
    for (char c : s) {
        if (c == '-') {
            parts.push_back(tmp); // tmp에는 수랑 +만 들어감
            // '-' 전까지 한 수마다 저장해둔 부호 없는 숫자 덩어리를 벡터에 저장
            tmp = "";  
            // 다음 덩어리를 위해 초기화
        } else {
            tmp += c;  
            // 숫자 또는 '+'를 현재 덩어리에 이어붙임
        }
    }
    parts.push_back(tmp);  
    // 마지막 덩어리도 벡터에 추가 (마지막은 숫자로 끝나니까 -가 나오지 않아서 parts에 자동 푸시 안됨)

    int ans = 0;  

    // 첫 번째 덩어리: 첫 문자는 숫자이므로 양수 → 그냥 더하기
    // stringstream은 string 저장 상태 + cursor + 읽기 상태
    // getline을 이용해 숫자를 연산기호 기준으로 분리해서 저장하기 위하여 사용.
    stringstream ss(parts[0]);  
    string num;
    while (getline(ss, num, '+')) { // +로 다른 수들과 추가로 연결되는 경우 
        // '+'를 기준으로 분리하여 ss의 각 항의 숫자들을 추출
        ans += stoi(num);  
        // 추출한 문자열 숫자를 정수로 변환 후 더함
    }

    // 두 번째 덩어리부터: 첫 '-' 이후 → 괄호 안의 덧셈들이므로 모두 빼기
    for (int i = 1; i < parts.size(); i++) {
        stringstream ss2(parts[i]);  
        // 덩어리를 '+' 기준으로 분리하며 읽기 위한 스트림
        while (getline(ss2, num, '+')) {  // 어차피 위 
            // '+'를 기준으로 숫자를 하나씩 추출
            ans -= stoi(num);  
            // 추출한 숫자를 정수로 변환 후 빼기
        }
    }

    cout << ans;  
    // 계산된 최소값 출력
}
