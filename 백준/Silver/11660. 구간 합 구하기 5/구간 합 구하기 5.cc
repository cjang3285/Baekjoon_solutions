#include <bits/stdc++.h>
using namespace std;

int N, M;
int A[1025][1025];
int P[1025][1025]; // P[i][j] = (1,1) ~ (i,j)까지의 누적합

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    cin >> N >> M;

    // 입력: O(N^2)
    for (int i = 1; i <= N; i++) {
        for (int j = 1; j <= N; j++) {
            cin >> A[i][j];
        }
    }

    // ========================
    // 누적합 배열 생성 (Bottom-up 조립)
    // 작은 직사각형들을 조립해서
    // (1,1) ~ (i,j) 큰 직사각형을 만든다.
    //
    // P[i-1][j] : 위쪽 덩어리
    // P[i][j-1] : 왼쪽 덩어리
    // P[i-1][j-1] : 두 덩어리에 겹쳐서 두 번 들어간 영역
    // A[i][j] : 아직 포함 안 된 마지막 한 칸
    //
    // => 겹친 건 한 번 빼고, 빠진 건 더한다 (포함-배제)
    //
    // 시간: O(N^2)
    // ========================
    for (int i = 1; i <= N; i++) { // 누적합 배열은 1 2 3 4 5 ... N
        for (int j = 1; j <= N; j++) { // 한 행마다 앞에서부터 순서대로 누적합을 계산하므로
            P[i][j] = P[i-1][j] // 이번 i, j 자리의 누적합(1,1,i,j)을 계산할 때 참조하는 이전 값들이 이전 pass에서 다 계산되어 있어 문제 없음
                    + P[i][j-1]
                    - P[i-1][j-1]
                    + A[i][j];
        }
    }

    // ========================
    // 질의 처리 (Top-down 잘라내기)
    //
    // 원하는 구간:
    // (x1, y1) ~ (x2, y2)
    //
    // 큰 전체에서 필요 없는 위/왼쪽을 빼고,
    // 두 번 빠진 왼쪽-위를 다시 더한다.
    //
    // S(x1,y1,x2,y2)
    // = P[x2][y2]
    // - P[x1-1][y2]   // x1-1 : 위로 한 칸 올라간 행까지
    // - P[x2][y1-1]   // y1-1 : 왼쪽으로 한 칸 간 열까지
    // + P[x1-1][y1-1] // 위+왼쪽이 두 번 빠졌으므로 복구
    //
    // 각 쿼리: O(1)
    // 전체: O(M)
    // 기존 완전탐색: O(N^2 * M) → 시간초과
    // ========================
    while (M--) {
        int x1, y1, x2, y2;
        cin >> x1 >> y1 >> x2 >> y2;

        int ans = P[x2][y2]
                - P[x1-1][y2]
                - P[x2][y1-1]
                + P[x1-1][y1-1];

        cout << ans << '\n';
    }

    /*
    전체 시간복잡도:
    입력 O(N^2)
    누적합 계산 O(N^2)
    질의 처리 O(M)

    => 총 O(N^2 + M)
    */
    return 0;
}
